fun main () {
    var panjang: Int = 10
    var lebar: Int = 5
    println(panjang)
    println(lebar)

    var nama: String = "Haris Miftahul Hudha"
    println(nama)

    var celcius: Float = 2.5f
    println(celcius)

    var fahrenheit: Double = 3.5
    println(fahrenheit)

    var apakahBerhasil: Boolean = false
    println(apakahBerhasil)

    var abjad: Char = 'a'
    println(abjad)
}

